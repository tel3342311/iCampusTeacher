package com.liteon.com.icampusteacher.util;

public class TargetItem {
	String carlos;
	String step;
	String walking;
	String running;
	String cycling;
	String sleep;
	
	/**
	 * @return the carlos
	 */
	public String getCarlos() {
		return carlos;
	}
	/**
	 * @param carlos the carlos to set
	 */
	public void setCarlos(String carlos) {
		this.carlos = carlos;
	}
	/**
	 * @return the step
	 */
	public String getStep() {
		return step;
	}
	/**
	 * @param step the step to set
	 */
	public void setStep(String step) {
		this.step = step;
	}
	/**
	 * @return the walking
	 */
	public String getWalking() {
		return walking;
	}
	/**
	 * @param walking the walking to set
	 */
	public void setWalking(String walking) {
		this.walking = walking;
	}
	/**
	 * @return the running
	 */
	public String getRunning() {
		return running;
	}
	/**
	 * @param running the running to set
	 */
	public void setRunning(String running) {
		this.running = running;
	}
	/**
	 * @return the cycling
	 */
	public String getCycling() {
		return cycling;
	}
	/**
	 * @param cycling the cycling to set
	 */
	public void setCycling(String cycling) {
		this.cycling = cycling;
	}
	/**
	 * @return the sleep
	 */
	public String getSleep() {
		return sleep;
	}
	/**
	 * @param sleep the sleep to set
	 */
	public void setSleep(String sleep) {
		this.sleep = sleep;
	}
}
